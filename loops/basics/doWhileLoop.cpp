#include <iostream>
using namespace std;

// DO WHILE LOOP :-
// kam se kam aik bar loop ko chlane ke liay DO WHILE LOOP Use hota ha

int main()
{

    // syntax of DO WHILE LOOP

    int i = 1; // initialization
    do
    {
        cout << i << endl;
        i++; // increment
    } while (i <= 10); // condition
}
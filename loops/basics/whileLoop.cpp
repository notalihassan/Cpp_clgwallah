#include <iostream>
using namespace std;

// WHILE LOOP :-
// 1. alternative to FOR LOOP
// 2. used for unknown condition or in wierd scenarios
// 3. smae as For lOOP

int main()
{

    // syntax of WHILE LOOP

    int i = 1;      // initialization or declaration
    while (i <= 10) // condition
    {
        cout << " ";
        i++; // increment or decrement
    }

    // syntax of FOR LOOP

    for (int i = 0; i <= 10; i++) // initialization; condition; increment;
    {
        cout << " ";
    }
}